create table license(
    license_id integer GENERATED ALWAYS AS IDENTITY,
    license_number varchar(256),
    state varchar(64),
    age integer,
    primary key(license_id),
    CONSTRAINT dln_unique UNIQUE (license_number)
);

create table discount(
    discount_id integer,
    percent_off numeric(3),
    description varchar(512),
    primary key(discount_id)
);

create table membership(
    membership_id integer GENERATED ALWAYS AS IDENTITY,
    group_name varchar(256),
    discount_id integer,
    foreign key(discount_id) references discount(discount_id),
    primary key(membership_id),
    CONSTRAINT mid_unique UNIQUE (group_name)
);

create table customer (
    customer_id integer GENERATED ALWAYS AS IDENTITY,
    address varchar(256),
    name varchar(256),
    license_id integer,
    membership_id integer,
    foreign key(license_id) references license(license_id),
    foreign key(membership_id) references membership(membership_id),
    primary key(customer_id)
);

create table location(
    location_id integer,
    street varchar(128),
    city varchar(128),
    state varchar(128),
    zip integer,
    primary key(location_id)
);

create table vehicle (
    vehicle_id integer,
    make varchar(64),
    model varchar(64),
    year number(4),
    color varchar(16),
    type varchar(16),
    vin varchar(17),
    odo integer,
    location_id integer,
    foreign key(location_id) references location(location_id),
    primary key(vehicle_id)
);

create table rental(
    rental_id integer,
    taken date,
    returned date,
    rate numeric(10,4),
    customer_id integer,
    vehicle_id integer,
    foreign key(customer_id) references customer(customer_id),
    foreign key(vehicle_id) references vehicle(vehicle_id),
    primary key(rental_id)
);

create table reservation (
    reservation_id integer,
    created date,
    customer_id integer,
    vehicle_id integer,
    foreign key(customer_id) references customer(customer_id),
    foreign key(vehicle_id) references vehicle(vehicle_id),
    primary key(reservation_id)
);

create table payment_method (
    payment_method_id integer,
    type varchar(128),
    card_number varchar(128),
    cvv varchar(16),
    exp varchar(16),
    customer_id integer,
    foreign key(customer_id) references customer(customer_id),
    primary key(payment_method_id)
);

create table charge(
    charge_id integer,
    fuel numeric(10,4),
    dropoff numeric(10,4),
    insurance numeric(10,4),
    other numeric(10,4),
    complete number(1),
    rental_id integer,
    foreign key(rental_id) references rental(rental_id),
    primary key(charge_id)
);
